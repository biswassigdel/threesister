    public ArrayList<BusTicket> busInfoList() {
        ArrayList<BusTicket> busTicketlList = new ArrayList<>();
        try {
            Connection con = null;
            ResultSet rs = null;
            PreparedStatement ps = null;

            String sql = "select * from BusTicket where Deleted='N'";

            con = DbManager.getConnection();
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            BusTicket busTicket = null;
            while (rs.next()) {
                busTicket = new BusTicket(rs.getInt("ID"),
                        rs.getString("Name"),
                        rs.getString("TravelAgency"),
                        rs.getString("IssueDate"),
                        rs.getString("DepartureDate"),
                        rs.getString("TicketNumber"));
                busTicketlList.add(busTicket);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return busTicketlList;
    }

    public void showBusInfoList() {
        ArrayList<BusTicket> list = busInfoList();
        DefaultTableModel model = (DefaultTableModel) busInfoListTable.getModel();
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        busInfoListTable.setDefaultRenderer(String.class, centerRenderer);
        int sn = 1;
        for (int i = 0; i < list.size(); i++) {
            Object[] row = new Object[7];
            row[0] = sn++;
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getTravelAgency();
            row[3] = list.get(i).getIssueDate();
            row[4] = list.get(i).getDepartureDate();
            row[5] = list.get(i).getTicketNumber();
            row[6] = list.get(i).getId();
            model.addRow(row);
        }
    }

