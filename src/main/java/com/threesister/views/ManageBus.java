    private void addBusTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBusTicketActionPerformed
        String name = busName.getText();
        String travelAgency = travelAgencyName.getText();
        String issueDate = ((JTextField) issueDateChooser.getDateEditor().getUiComponent()).getText();
        String departureDate = ((JTextField) departureDateChooser.getDateEditor().getUiComponent()).getText();
        String ticketNumber = this.ticketNumber.getText();
        if (!"".equals(name) && !"".equals(travelAgency) && !"".equals(issueDate) && !"".equals(departureDate) && !"".equals(ticketNumber)) {
            try {
                boolean isAdded = AddBusTicketDAO.addBusTicket(name, travelAgency, issueDate, departureDate, ticketNumber);
                if (isAdded) {
                    DefaultTableModel model = (DefaultTableModel) busInfoListTable.getModel();
                    model.setRowCount(0);
                    showBusInfoList();
                    JOptionPane.showMessageDialog(null, "Bus info added sucessfully");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please provide all the necessary details");
        }
    }//GEN-LAST:event_addBusTicketActionPerformed

    private void busInfoListTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_busInfoListTableMouseClicked
        int i = busInfoListTable.getSelectedRow();

        TableModel model = busInfoListTable.getModel();
        busName.setText(model.getValueAt(i, 1).toString());
        travelAgencyName.setText(model.getValueAt(i, 2).toString());
        try {
            java.util.Date issueDate = new SimpleDateFormat("yyyy-MM-dd").parse((String) model.getValueAt(i, 3));
            issueDateChooser.setDate(issueDate);
            java.util.Date departureDate = new SimpleDateFormat("MMM d, yyyy").parse((String) model.getValueAt(i, 4));
            departureDateChooser.setDate(departureDate);
        } catch (ParseException ex) {
            Logger.getLogger(ManageBus.class.getName()).log(Level.SEVERE, null, ex);
        }
        ticketNumber.setText(model.getValueAt(i, 5).toString());
        id.setText(model.getValueAt(i, 6).toString());
    }//GEN-LAST:event_busInfoListTableMouseClicked

    private void updateBusTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBusTicketActionPerformed
        int row = busInfoListTable.getSelectedRow();
        int id = (int) (busInfoListTable.getModel().getValueAt(row, 6));
        String issueDate = ((JTextField) issueDateChooser.getDateEditor().getUiComponent()).getText();
        String departureDate = ((JTextField) departureDateChooser.getDateEditor().getUiComponent()).getText();
        try {
            Connection con = null;
            ResultSet rs = null;
            PreparedStatement ps = null;
            String sql = "update BusTicket set Name=?, TravelAgency=?, IssueDate=?, DepartureDate=?,TicketNumber=?  where id='" + id + "'";

            System.out.println(sql);
            con = DbManager.getConnection();
            ps = con.prepareStatement(sql);
            ps.setString(1, busName.getText());
            ps.setString(2, travelAgencyName.getText());
            ps.setString(3, issueDate);
            ps.setString(4, departureDate);
            ps.setString(5, ticketNumber.getText());
            ps.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) busInfoListTable.getModel();
            model.setRowCount(0);
            showBusInfoList();
            JOptionPane.showMessageDialog(null, "Bus info updated Sucessfully");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateBusTicketActionPerformed

