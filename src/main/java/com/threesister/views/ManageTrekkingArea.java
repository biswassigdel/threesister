    public ArrayList<TrekkingArea> staffList() {
        ArrayList<TrekkingArea> trekkingAreaList = new ArrayList<>();
        try {
            Connection con = null;
            ResultSet rs = null;
            PreparedStatement ps = null;

            String sql = "select * from TrekkingArea where Deleted='N'";

            con = DbManager.getConnection();
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            TrekkingArea trekkingArea;
            while (rs.next()) {
                trekkingArea = new TrekkingArea(rs.getInt("id"), rs.getString("TrekkingAreaName"), rs.getString("DurationDay"), rs.getString("DurationNight"), rs.getString("Route"));
                trekkingAreaList.add(trekkingArea);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return trekkingAreaList;
    }

    public void showTrekkingAreas() {
        ArrayList<TrekkingArea> list = staffList();
        DefaultTableModel model = (DefaultTableModel) trekkingAreaTableList.getModel();
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        trekkingAreaTableList.setDefaultRenderer(String.class, centerRenderer);
        int sn = 1;
        for (int i = 0; i < list.size(); i++) {
            Object[] row = new Object[6];
            row[0] = sn++;
            row[1] = list.get(i).getTrakkingAreaName();
            row[2] = list.get(i).getDays();
            row[3] = list.get(i).getNights();
            row[4] = list.get(i).getRoute();
            row[5] = list.get(i).getId();
            model.addRow(row);
        }
    }

