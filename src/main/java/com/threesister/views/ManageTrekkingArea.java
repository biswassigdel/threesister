    private void updateTrekkingAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTrekkingAreaActionPerformed
        int row = trekkingAreaTableList.getSelectedRow();
        int id = (int) (trekkingAreaTableList.getModel().getValueAt(row, 5));
        try {
            Connection con = null;
            ResultSet rs = null;
            PreparedStatement ps = null;
            String sql = "update TrekkingArea set TrekkingAreaName=?, DurationDay=?, DurationNight=?, Route=? where id='" + id + "'";

            System.out.println(sql);
            con = DbManager.getConnection();
            ps = con.prepareStatement(sql);
            ps.setString(1, trekkingAreaName.getText());
            ps.setString(2, durationDay.getText());
            ps.setString(3, durationNight.getText());
            ps.setString(4, route.getText());

            ps.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) trekkingAreaTableList.getModel();
            model.setRowCount(0);
            showTrekkingAreas();
            JOptionPane.showMessageDialog(null, "Trekking Area updated Sucessfully");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateTrekkingAreaActionPerformed

