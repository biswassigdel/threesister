    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String trekkingArea = trekkingAreaName.getText();
        String days = durationDay.getText();
        String nights = durationNight.getText();
        String routeName = route.getText();

        if (!"".equals(trekkingArea) && !"".equals(days) && !"".equals(nights) && !"".equals(routeName)) {
            boolean isAdded;
            isAdded = TrekkingAreaDao.addTrekkingArea(trekkingArea, days, nights, routeName);
            if (isAdded) {
                JOptionPane.showMessageDialog(null, "Trekking Area Added Sucessfully");
                DefaultTableModel model = (DefaultTableModel) trekkingAreaTableList.getModel();
                model.setRowCount(0);
                showTrekkingAreas();
            } else {
                JOptionPane.showMessageDialog(null, "Trekking Area Couldnot be added");
            }
        } else {
            JOptionPane.showMessageDialog(null, "please fil up the data");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

